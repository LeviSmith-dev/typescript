// +, -, *, /, %, ++, --

var numOne : number = 1;
var numTwo : number = 2;

//Addition
console.log("addition:");
console.log(numOne + numTwo);

//subtraction
console.log("subtraction:");
console.log(numOne - numTwo);

//multiplication
console.log("multiplication:");
numOne = 15;
numTwo = 10;
console.log(numOne * numTwo);

//Division

console.log("division:");
numOne = 10;
numTwo = 15;
console.log(numOne / numTwo);

// Modulus

console.log("modulus:");
var numThree : number = 2;
var numFour : number = 20;
console.log(numFour % numThree);

// Incrementor

console.log("incrementor:");
var x : number = 0;

while (x < 10) {
	console.log(x);
	x++;
}

// Decrementor
console.log("decrementor:");
var x : number = 10;

while (x > 0) {
	console.log(x);
	x--;
}




